export const deleteLast = (s, l) => {
  if (s.slice(-1) === l) {
    return s.slice(0, -1);
  } else {
    return s;
  }
};

/**
 * サービスが該当するかどうか (カンマ区切りの文字列に対応)
 */
export const inService = (svcsWithComma, svc) => {
  if (!svc) return true;
  if (typeof svcsWithComma === 'string') {
    return svcsWithComma.split(',').includes(svc);
  }
  return false;
};

