// ゼロ埋めを行う
export const zp = (n, l) => {
  n = n ? n : '0';
  let s = '0'.repeat(l);
  n = (s + n).slice(-l);
  return n;
};

// スペースで埋めて文字列長さを揃える
export const spfill = (str, l) => {
  str = str ? str : '';
  let s = ' '.repeat(l);
  return (str + s).slice(0, l);
};

